class ForecastDay:
    def __init__(self, date, high, low, preciptPM, preciptAM, forecast, forecastDate):
        self.date = date
        self.high = high
        self.low = low
        self.preciptPM = preciptPM
        self.preciptAM = preciptAM
        self.forecast = forecast
        self.forecastDate = forecastDate